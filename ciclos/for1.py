#inicia programa que muestre los numeros en un rango de 1 a 10 
for i in range(11): # inicia programa 
    print(i) #declarar i 
for j in range(1,11): # se muestra resultados de rango de 1 a 10    
    print(j) #declarar j 
for k in range(0,11,2):# se muestra resultado en incremento de 2 en 2     
    print(k) #declarar k
for i in range(10,-1,-2): # se muestra resultado en decremento de -2 en -2
    print(i) #declarar i    